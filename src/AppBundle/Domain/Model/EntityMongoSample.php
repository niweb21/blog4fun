<?php

namespace AppBundle\Domain\Model;

class EntityMongoSample
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $title;

    /**
     * @var string
     */
    private $someData;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle($title)
    {
        $this->title = $title;
    }

    /**
     * @return string
     */
    public function getSomeData()
    {
        return $this->someData;
    }

    /**
     * @param string $someData
     */
    public function setSomeData($someData)
    {
        $this->someData = $someData;
    }
}
